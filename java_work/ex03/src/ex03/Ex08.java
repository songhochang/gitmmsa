package ex03;

public class Ex08 {
	
	// alt + shift + n 파일만들기
	
			// & ^ > >> << ??
			
			// & -> 2진수 and 연산자
			// ^ -> not 연산자
			// >> -> 2진수 왼쪽 이동 연산자
			
			/* 2진수 -> 10진수
			 * 
			 * 1010 -> 10
			 * 1100 -> 12
			 * 
			 * 10 & 12 = 1000 = 8
			 */
	
	public static void main(String[] args) {
		
		System.out.println(10 & 12);
		System.out.println(10 ^ 12);
		
	}

}
